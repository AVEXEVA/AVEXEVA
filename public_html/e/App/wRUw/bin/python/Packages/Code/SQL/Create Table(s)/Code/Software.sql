CREATE TABLE Application (
  ID INT NOT NULL AUTO_INCREMENT,
  Name VARCHAR(256),
  Description TEXT,
  CONSTRAINT PK_Application_ID PRIMARY KEY (ID)
);
CREATE TABLE Virtual_Server (
  ID INT NOT NULL AUTO_INCREMENT,
  Server INT,
  Backup INT,
  CONSTRAINT PK_Virtual_Server_ID PRIMARY KEY (ID),
  CONSTRAINT PK_Virtual_Server_Server FOREIGN KEY (Server) REFERENCES Server(ID),
  CONSTRAINT PK_Virtual_Server_Backup FOREIGN KEY (File) REFERENCES File(ID)
);
CREATE TABLE Operating_System (
  ID INT NOT NULL AUTO_INCREMENT,
  Name VARCHAR(256),
  Version VARCHAR(256),
  CONSTRAINT PK_Operating_System_ID PRIMARY KEY (ID)
);
CREATE TABLE Software (
  ID INT NOT NULL AUTO_INCREMENT,
  Name VARCHAR(256),
  Description TEXT,
  Product INT,
  CONSTRAINT PK_Software_ID PRIMARY KEY (ID)
  CONSTRAINT FK_Software_Product FOREIGN KEY (Product) REFERENCES Product(ID)
);

CREATE TABLE API (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_API_ID PRIMARY KEY (ID)
);
CREATE TABLE Module (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Module_ID PRIMARY KEY (ID)
);
CREATE TABLE Add_On (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Add_On_ID PRIMARY KEY (ID)
);
CREATE TABLE Code (
  ID INT NOT NULL AUTO_INCREMENT,
  Name VARCHAR(256),
  Description TEXT,
  CONSTRAINT PK_Code_ID PRIMARY KEY (ID)
);
CREATE TABLE Script (
  ID INT NOT NULL AUTO_INCREMENT,
  Name VARCHAR(256),
  Description TEXT,
  CONSTRAINT PK_Script_ID PRIMARY KEY (ID)
);
