CREATE TABLE `Connection` (
  `ID`      INT NOT NULL AUTO_INCREMENT,
  `User`    INT NOT NULL,
  `Agent`   INT NOT NULL,
  `Secret`  VARCHAR(256) NOT NULL,
  `Created` DATETIME DEFAULT CURRENT_TIMESTAMP,
  `Active`  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT `PK_Connection_ID`     PRIMARY KEY (`ID`)
  CONSTRAINT `FK_Connection_User`   FOREIGN KEY (`User`)  REFERENCES User(`ID`),
  CONSTRAINT `FK_Connection_Agent`  FOREIGN KEY (`Agent`) REFERENCES Agent(`ID`)
) ENGINE = MyISAM;
