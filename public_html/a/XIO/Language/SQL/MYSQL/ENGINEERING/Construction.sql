CREATE TABLE Wall (
  ID INT NOT NULL AUTO_INCREMENT,
  Material INT,
  CONSTRAINT PK_Wall_ID PRIMARY KEY (ID)
  CONSTRAINT FK_Wall_Material FOREIGN KEY (Material) REFERENCES Material(ID)
);
CREATE TABLE Floor (
  ID INT NOT NULL AUTO_INCREMENT,
  Material INT,
  CONSTRAINT PK_Floor_ID PRIMARY KEY (ID)
  CONSTRAINT FK_Floor_Material FOREIGN KEY (Material) REFERENCES Material(ID)
);
CREATE TABLE Window (
  ID INT NOT NULL AUTO_INCREMENT,
  Material INT,
  CONSTRAINT PK_Window_ID PRIMARY KEY (ID),
  CONSTRAINT FK_Window_Material FOREIGN KEY (Material) REFERENCES Material(ID)
);
CREATE TABLE Door (
  ID INT NOT NULL AUTO_INCREMENT,
  Material INT,
  CONSTRAINT PK_Door_ID PRIMARY KEY (ID)
  CONSTRAINT FK_Door_Material FOREIGN KEY (Material) REFERENCES Material(ID)
);
CREATE TABLE Beam (
  ID INT NOT NULL AUTO_INCREMENT,
  Material INT,
  CONSTRAINT PK_Beam_ID PRIMARY KEY (ID)
  CONSTRAINT FK_Beam_Material FOREIGN KEY (Material) REFERENCES Material(ID)
);
