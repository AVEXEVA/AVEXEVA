CREATE TABLE Stock (
  ID INT NOT NULL AUTO_INCREMENT,
  Company INT NOT NULL,
  Owner INT NOT NULL,
  CONSTRAINT PK_Stock_ID PRIMARY KEY (ID),
  CONSTRAINT FK_Stock_Company FOREIGN KEY (Company) REFERENCES Company(ID),
  CONSTRAINT FK_Stock_Owner FOREIGN KEY (Owner) REFERENCES Entity(ID)
);
CREATE TABLE Commodity (
  ID INT NOT NULL AUTO_INCREMENT,
  Item INT,
  CONSTRAINT PK_Commodity_ID PRIMARY KEY (ID),
  CONSTRAINT FK_Commodity_Item FOREIGN KEY Item(ID)
);
CREATE TABLE Currency (
  ID INT NOT NULL AUTO_INCREMENT,
  Name VARCHAR(256),
  Description TEXT,
  CONSTRAINT PK_Currency_ID PRIMARY KEY (ID)
);
