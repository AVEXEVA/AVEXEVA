CREATE TABLE Belief (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Belief_ID PRIMARY KEY (ID)
);
CREATE TABLE Testament (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Testament_ID PRIMARY KEY (ID)
);
CREATE TABLE Name (
  ID INT NOT NULL AUTO_INCREMENT,
  Characters VARCHAR(256),
  CONSTRAINT PK_Name_ID PRIMARY KEY (ID)
);
CREATE TABLE Person (
  ID INT NOT NULL AUTO_INCREMENT,
  Name INT,
  CONSTRAINT PK_Person_ID PRIMARY KEY (ID),
  CONSTRAINT FK_Person_Name FOREIGN KEY (Name) REFERENCES Name(ID)
);
CREATE TABLE Religion (
  ID INT NOT NULL AUTO_INCREMENT,
  Name VARCHAR(256),
  Description TEXT,
  CONSTRAINT PK_Religion_ID PRIMARY KEY (ID),
);
CREATE TABLE Founder (
  Religion INT,
  Person INT,
  CONSTRAINT FK_Founder_Religion FOREIGN KEY (Religion) REFERENCES Religion(ID),
  CONSTRAINT FK_Founder_Person FOREIGN KEY (Person) REFERENCES Person(ID)
);
