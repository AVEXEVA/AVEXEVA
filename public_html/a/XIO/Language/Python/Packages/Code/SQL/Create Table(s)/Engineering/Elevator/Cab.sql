CREATE TABLE Cab (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Cab_ID PRIMARY KEY (ID)
);
CREATE TABLE Operating_Panel (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Operating_Panel PRIMARY KEY (ID)
);
CREATE TABLE Operating_Panel_Button (
  Operating_Panel INT NOT NULL,
  Button INT NOT NULL,
  CONSTRAINT FK_Operating_Panel_Button_Operating_Panel FOREIGN KEY (Operating_Panel) REFERENCES Operating_Panel(ID),
  CONSTRAINT FK_Operating_Panel_Button_Button FOREIGN KEY (Button) REFERENCES Button(ID)
);
CREATE TABLE Camera (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Camera_ID PRIMARY KEY (ID)
);
CREATE TABLE Saddle (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Saddle_ID PRIMARY KEY (ID)
);
CREATE TABLE Handrail (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Handrail_ID PRIMARY KEY (ID)
);
CREATE TABLE Cab_Wall (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Cab_Wall_ID PRIMARY KEY (ID)
);
