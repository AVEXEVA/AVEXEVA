CREATE TABLE Patch (
  ID INT NOT NULL AUTO_INCREMENT,
  Version INT,
  CONSTRAINT PK_Patch_ID PRIMARY KEY (ID),
  CONSTRAINT FK_Patch_Version FOREIGN KEY Version(ID)
);
CREATE TABLE Version (
  ID INT NOT NULL AUTO_INCREMENT,
  Sequence VARCHAR(256)
  CONSTRAINT PK_Version_ID PRIMARY KEY (ID)
);
CREATE TABLE Update (
  ID INT NOT NULL AUTO_INCREMENT,
  CONSTRAINT PK_Update_ID PRIMARY KEY (ID)
);
